    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        this.second = Float.parseFloat(this.casilla.getText());
        float res = 0;
        switch(this.operator){
            case "+":
                res = this.first + this.second;
                break;
            case "-":
                res = this.first - this.second;
                break;
            case "*":
                res = this.first * this.second;
                break;
            case "/":
                if(this.second == 0) this.casilla.setText("Divide by 0");
                else res = this.first / this.second;
                break;
        }
        String result = Float.toString(res);
        if(res % 1 == 0)
            result = result.substring(0, result.length() - 2);
        this.casilla.setText(result);
    }//GEN-LAST:event_jButton14ActionPerformed

